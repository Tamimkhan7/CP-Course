#include<bits/stdc++.h>
using namespace std;

int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL); // unties cin from cout, so it will not flush cout before taking input from cin, so output will be shown after taking all inputs
    int n;
    cin >> n;
    for (int i = 1; i <= n; i++)
    {
        int x;
        cin >> x;
        cout << "x = " << x << '\n';
    }
    return 0;
}
